    private void buscar(String valor){
 try {
 cursor = (DefaultTableModel) this.tblEspecialidades.getModel();
 String sql = "select Codigo, Descripcion FROM especialidades"
 + " WHERE Descripcion like '%"+valor+"%' order by codigo";
 rs1 = con.listar_datos(sql);
 String[] fila1 = new String [2]; // cantidad de campos para la grilla
 while (rs1.next()) {
 fila1[0] = rs1.getString("Codigo");
 fila1[1] = rs1.getString("Descripcion");
 cursor.addRow(fila1);
 }
 } catch (SQLException ex) {
 Logger.getLogger(Especialistas.class.getName()).log(Level.SEVERE,null,ex);
 }
}
// Limpia la grilla
private void limpiargrilla() {
 cursor = (DefaultTableModel) this.tblEspecialidades.getModel();
 while(cursor.getRowCount() > 0) {
 cursor.removeRow(0);
 }
}
