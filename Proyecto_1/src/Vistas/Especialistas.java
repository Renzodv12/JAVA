private void habilitar_boton(int a) {
 switch (a) {
 case 1:
 this.btnNuevo.setEnabled(false);
 this.btnModificar.setEnabled(false);
 this.btnGrabar.setEnabled(false);
 this.btnCancelar.setEnabled(true);
 this.btnSalir.setEnabled(true);
 this.btnEliminar.setEnabled(false);
 break;
 case 2:
 this.btnNuevo.setEnabled(true);
 this.btnModificar.setEnabled(true);
 this.btnGrabar.setEnabled(false);
 this.btnCancelar.setEnabled(true);
 this.btnSalir.setEnabled(true);
 this.btnEliminar.setEnabled(true);
 this.txtCodigo.setEnabled(false);
 this.txtDescripcion.setEnabled(false);
 break;
 }
}
// Genera el codigo nuevo
private void generar_cod() {
 try {
 String sql = "select coalesce(max(Codigo),0)+1 as cod from especialidades ";
 rs = con.listar_datos(sql);
 rs.next();
 this.txtCodigo.setText(rs.getString("cod"));
 } catch (SQLException ex) {
 Logger.getLogger(Especialistas.class.getName()).log(Level.SEVERE, null, ex);
 }
}
